    private void buttonFinalizarAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarAlteracaoActionPerformed
        comboBoxNovoTipoDeUsuarioActionPerformed(evt); //to make sure it's selected

        LoginTable selectedLogin = (LoginTable) comboBoxUsuario.getSelectedItem();

        trimMandatoryFields();

        if (selectedLogin.isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Finalizar Alteração\", favor selecionar uma categoria de filme, e clicar em \"Buscar\".",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (textFieldNovoLogin.getText().isEmpty()
                || textFieldNovaSenha.getText().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nOs campos * são obrigatórios!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (textFieldNovoLogin.getText().length() < 4) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nO login precisa ter mais de 3 caracteres!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (textFieldNovaSenha.getText().length() < 8) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nA senha precisa ter mais de 7 caracteres!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (!textFieldNovoLogin.getText().equals(selectedLogin.getLogin())
                && comboBoxUsuarioContainsLogin(textFieldNovoLogin.getText())) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nO Login \"" + textFieldNovoLogin.getText() + "\" não está disponível!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (JOptionPane.showOptionDialog(
                null,
                "Tem certeza que deseja finalizar?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new String[]{"Sim", "Não"},
                "Sim") == JOptionPane.YES_OPTION) {

            try {
                UserTypeTable selectedUserType = (UserTypeTable) comboBoxNovoTipoDeUsuario.getSelectedItem();

                //updating TB_LOGIN
                prepStmt = conn.prepareStatement("UPDATE TB_LOGIN SET"
                        + " LOG_LOGIN = ?," //1
                        + " LOG_PASSWORD = ?," //2
                        + " LOG_NOTES = ?," //3
                        + " LOG_ACTIVE = ?," //4
                        + " LOG_TB_USER_TYPE_ID = ?" //5
                        + " WHERE LOG_ID = ?"); //6
                prepStmt.setString(1, textFieldNovoLogin.getText());
                prepStmt.setString(2, textFieldNovaSenha.getText());
                prepStmt.setString(3, textAreaNovasAnotacoes.getText());
                prepStmt.setInt(4, checkBoxUsuarioAtivo.isSelected() ? 1 : 0);
                prepStmt.setInt(5,
                        !selectedUserType.isEmptyString() //if some userType is selected
                        ? selectedUserType.getId() //userType changed
                        : selectedLogin.getTbUserTypeId() //userType didnt change
                );
                prepStmt.setInt(6, selectedLogin.getId());
                prepStmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Alteração do Usuário bem sucedida.");
                new AdminGerenciadorDeEmprestimosDeFilmes().setVisible(true);
                super.dispose();

            } catch (SQLException e) {
                System.out.println(e.getMessage());
            } finally {
                try {
                    if (conn != null) {
                        conn.close();
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_buttonFinalizarAlteracaoActionPerformed

