    public AlterarUsuario() {
        comboBoxUsuarioDataList = new Vector();
        comboBoxUsuario = new AutoComboBox(comboBoxUsuarioDataList);
        comboBoxNovoTipoDeUsuarioDataList = new Vector();
        comboBoxNovoTipoDeUsuario = new AutoComboBox(comboBoxNovoTipoDeUsuarioDataList);

        initComponents();

        try {
            conn = DriverManager.getConnection("jdbc:sqlite:local/designedDb.db");
            stmt = conn.createStatement();
            stmt.setQueryTimeout(30);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        //populate comboBoxBuscarUsuario
        comboBoxUsuarioDataList.add(new LoginTable()); //insert the first empty string
        try {
            prepStmt = conn.prepareStatement("SELECT *"
                    + " FROM TB_LOGIN"
                    + " ORDER BY LOG_LOGIN");
            rs = prepStmt.executeQuery();

            while (rs.next()) {
                LoginTable current = new LoginTable(
                        rs.getInt("LOG_ID"),
                        rs.getString("LOG_LOGIN"),
                        rs.getString("LOG_PASSWORD"),
                        rs.getString("LOG_NOTES"),
                        rs.getInt("LOG_ACTIVE"),
                        rs.getInt("LOG_TB_USER_TYPE_ID")
                );

                //getting the userType description of the current login
                prepStmt = conn.prepareStatement("SELECT USE_TYP_DESCRIPTION"
                        + " FROM TB_USER_TYPE"
                        + " WHERE USE_TYP_ID = ?"); //1
                prepStmt.setInt(1, current.getTbUserTypeId());
                current.setUserTypeDescription(prepStmt.executeQuery().getString("USE_TYP_DESCRIPTION"));

                comboBoxUsuarioDataList.add(current);
            }

            comboBoxUsuario.setDataList(comboBoxUsuarioDataList);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        //populate comboBoxNovoTipoDeUsuario
        comboBoxNovoTipoDeUsuarioDataList.add(new UserTypeTable()); //insert the first empty string
        try {
            prepStmt = conn.prepareStatement("SELECT *"
                    + " FROM TB_USER_TYPE"
                    + " WHERE USE_TYP_ACTIVE = ?" //1
                    + " ORDER BY USE_TYP_DESCRIPTION");
            prepStmt.setInt(1, 1);
            rs = prepStmt.executeQuery();

            while (rs.next()) {
                comboBoxNovoTipoDeUsuarioDataList.add(new UserTypeTable(
                        rs.getInt("USE_TYP_ID"),
                        rs.getString("USE_TYP_DESCRIPTION"),
                        rs.getString("USE_TYP_NOTES"),
                        rs.getInt("USE_TYP_ACTIVE")
                ));
            }

            comboBoxNovoTipoDeUsuario.setDataList(comboBoxNovoTipoDeUsuarioDataList);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

