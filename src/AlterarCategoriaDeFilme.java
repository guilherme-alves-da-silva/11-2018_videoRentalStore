    /**
     * checking if the description is already present in the comboBox list.
     * this comboBox list has all the movieCategories from the DB, movieCategories that are active
     * and inactive, therefore is the same as searching the DB, but faster.
     *
     * is not nice to have to create a MovieCategoryTable object just to compare their description
     * attribute, therefore it wouldn't work if i used a custom equals(), that compares their
     * description attribute, in the MovieCategoryTable object.
     *
     * @param description
     * @return
     */
    private boolean comboBoxCategoriaDeFilmeContainsDescription(String description) {
        for (int i = 0; i < comboBoxCategoriaDeFilmeDataList.size(); i++) {
            MovieCategoryTable currentMovieCategory = (MovieCategoryTable) comboBoxCategoriaDeFilmeDataList.get(i);

            if (!currentMovieCategory.isEmptyString()
                    && currentMovieCategory.getDescription().equals(description)) {

                return true;
            }
        }

        return false;
    }

