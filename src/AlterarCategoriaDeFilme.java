    private void buttonFinalizarAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarAlteracaoActionPerformed
        MovieCategoryTable selectedMovieCategory = (MovieCategoryTable) comboBoxCategoriaDeFilme.getSelectedItem();

        //trimming
        textFieldNovaDescricao.setText(textFieldNovaDescricao.getText().trim());

        if (selectedMovieCategory.isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Finalizar Alteração\", favor selecionar uma categoria de filme, e clicar em \"Buscar\".",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (textFieldNovaDescricao.getText().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nOs campos * são obrigatórios!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (!textFieldNovaDescricao.getText().equals(selectedMovieCategory.getDescription())
                && comboBoxCategoriaDeFilmeContainsDescription(textFieldNovaDescricao.getText())) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nJá existe uma categoria de filme chamada \"" + textFieldNovaDescricao.getText() + "\" !",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (JOptionPane.showOptionDialog(
                null,
                "Tem certeza que deseja finalizar?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new String[]{"Sim", "Não"},
                "Sim") == JOptionPane.YES_OPTION) {

            try {
                //updating TB_MOVIE_CATEGORY
                prepStmt = conn.prepareStatement("UPDATE TB_MOVIE_CATEGORY SET"
                        + " MOV_CAT_DESCRIPTION = ?," //1
                        + " MOV_CAT_NOTES = ?," //2
                        + " MOV_CAT_ACTIVE = ?" //3
                        + " WHERE MOV_CAT_ID = ?"); //4
                prepStmt.setString(1, textFieldNovaDescricao.getText());
                prepStmt.setString(2, textAreaNovasAnotacoes.getText());
                prepStmt.setInt(3, checkBoxCategoriaDeFilmeAtiva.isSelected() ? 1 : 0);
                prepStmt.setInt(4, selectedMovieCategory.getId());
                prepStmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Alteração da Categoria de Filme bem sucedida.");
                new GerenciadorDeEmprestimosDeFilmes().setVisible(true);
                super.dispose();

            } catch (SQLException e) {
                System.out.println(e.getMessage());
            } finally {
                try {
                    if (conn != null) {
                        conn.close();
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_buttonFinalizarAlteracaoActionPerformed

