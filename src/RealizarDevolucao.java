    private void buttonBuscarEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarEmprestimoActionPerformed
        RentalTable selectedRental = (RentalTable) comboBoxEmprestimo.getSelectedItem();

        if (((ClientTable) comboBoxCliente.getSelectedItem()).isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Buscar Empréstimo\", favor selecionar um cliente, e clicar em \"Buscar Cliente\".",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (selectedRental.isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Buscar Empréstimo\", favor selecionar um empréstimo para buscar.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else {
            listFilmesModel.clear();

            /*
             * populate listFilmes.
             * 
             * " WHERE MOV_AVAILABLE = 0" because the client may want to return some movies, of the
             * rent, before the due date, therefore some of the movies connected to the rent may
             * already be available, because the client returned them already.
             *
             * OBS: i'm using this indentation in the string instead of it being before the "+"
             * so that the IDE wont pull it back when i use the autoFormat from the IDE.
             */
            try {
                prepStmt = conn.prepareStatement("SELECT *"
                        + " FROM TB_MOVIE"
                        + " WHERE MOV_AVAILABLE = 0"
                        + " AND MOV_ID IN ("
                        + "                     SELECT REN_MOV_TB_MOVIE_ID"
                        + "                     FROM TB_RENTAL_MOVIE"
                        + "                     WHERE REN_MOV_TB_RENTAL_ID = ?" //1
                        + ")");
                prepStmt.setInt(1, selectedRental.getId());
                rs = prepStmt.executeQuery();

                while (rs.next()) {
                    listFilmesModel.addElement(new MovieTable(
                            rs.getInt("MOV_ID"),
                            rs.getString("MOV_NAME"),
                            rs.getString("MOV_YEAR_OF_RELEASE"),
                            rs.getLong("MOV_DATE_OF_ACQUISITION"),
                            rs.getString("MOV_DESCRIPTION"),
                            rs.getInt("MOV_AVAILABLE"),
                            rs.getString("MOV_NOTES"),
                            rs.getString("MOV_KIND_OF_MEDIA"),
                            rs.getInt("MOV_DAMAGED"),
                            rs.getInt("MOV_ACTIVE"),
                            rs.getInt("MOV_TB_PRICE_CATEGORY_ID")
                    ));
                }
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }

            labelValueDiasDeAtraso.setText(
                    (selectedRental.getAmountOfDays() > selectedRental.getAmountOfDaysPredicted()
                    ? selectedRental.getAmountOfDays() - selectedRental.getAmountOfDaysPredicted()
                    : "0") + " dia(s)"
            );

            updateLabelValueValorTotal();
        }
    }//GEN-LAST:event_buttonBuscarEmprestimoActionPerformed

