    private Long getMoviePrice(int priceCategoryID, int amountOfDays) {
        Long price = -1L;

        try {
            prepStmt = conn.prepareStatement("SELECT PRI_PRICE"
                    + " FROM TB_PRICE"
                    + " WHERE PRI_AMOUNT_OF_DAYS = ?" //1
                    + " AND PRI_TB_PRICE_CATEGORY_ID = ?"); //2
            prepStmt.setInt(1, amountOfDays);
            prepStmt.setInt(2, priceCategoryID);
            rs = prepStmt.executeQuery();

            if (rs.next()) {
                price = rs.getLong("PRI_PRICE");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        return price;
    }

    private String updateLabelValueValorTotal() {
        RentalTable selectedRental = (RentalTable) comboBoxEmprestimo.getSelectedItem();
        Long totalPrice = 0L;

        //this is updating the valorTotalValueLabel
        //by getting the price of each movie in the filmesList from the db everytime, it's
        //costly in resources if the filmesList is long, but most of the time it
        //will be 0 < size < 3 tops. i could implement a list of the prices of the movies currently
        //in the list instead of getting it again from the db, but since the list wont be long most
        //of the time and this teacher apparently doesnt care about code, it's not worth it atm.
        for (int i = 0; i < listFilmesModel.getSize(); i++) {
            MovieTable currentMovie = (MovieTable) listFilmesModel.getElementAt(i);

            Long moviePricePredictedReturn;
            Long finePrice = 0L;
            int multiplyBy = 0;
            Long moviePriceLateReturn = 0L;

            int amountOfDaysPredicted = selectedRental.getAmountOfDaysPredicted();
            moviePricePredictedReturn = getMoviePrice(currentMovie.getTbPriceCategoryId(), amountOfDaysPredicted);

            /*
             * if the priceCategory of the movie doesnt have a price for the amountOfDays, find a
             * day, lower than the current amounrOfDays, that has a price.
             */
            while (moviePricePredictedReturn.equals(new Long("-1")) && amountOfDaysPredicted > 0) {
                amountOfDaysPredicted--;

                moviePricePredictedReturn = getMoviePrice(currentMovie.getTbPriceCategoryId(), amountOfDaysPredicted);
            }

            if (moviePricePredictedReturn.equals(new Long("-1")) && amountOfDaysPredicted == 0) {
                moviePricePredictedReturn = 0L;
            }

            //if this is a late return
            int amountOfDaysAfterPredictedReturn = selectedRental.getAmountOfDays() - selectedRental.getAmountOfDaysPredicted();
            if (amountOfDaysAfterPredictedReturn > 0) {
                //get data from the priceCategory of the current movie
                try {
                    prepStmt = conn.prepareStatement("SELECT PRI_CAT_FINE_PRICE,"
                            + " PRI_CAT_MULTIPLY_BY"
                            + " FROM TB_PRICE_CATEGORY"
                            + " WHERE PRI_CAT_ID = ?"); //1
                    prepStmt.setInt(1, currentMovie.getTbPriceCategoryId());
                    rs = prepStmt.executeQuery();

                    if (rs.next()) {
                        finePrice = rs.getLong("PRI_CAT_FINE_PRICE");
                        multiplyBy = rs.getInt("PRI_CAT_MULTIPLY_BY");
                    }
                } catch (SQLException e) {
                    System.out.println(e.getMessage());
                }

                //get the price for the days after the predicted return
                moviePriceLateReturn = getMoviePrice(currentMovie.getTbPriceCategoryId(), amountOfDaysAfterPredictedReturn);

                /*
                 * if the priceCategory of the movie doesnt have a price for the amountOfDays, find a
                 * day, lower than the current amounrOfDays, that has a price.
                 */
                while (moviePriceLateReturn.equals(new Long("-1")) && amountOfDaysAfterPredictedReturn > 0) {
                    amountOfDaysAfterPredictedReturn--;

                    moviePriceLateReturn = getMoviePrice(currentMovie.getTbPriceCategoryId(), amountOfDaysAfterPredictedReturn);
                }

                if (moviePriceLateReturn.equals(new Long("-1")) && amountOfDaysAfterPredictedReturn == 0) {
                    moviePriceLateReturn = 0L;
                }
            }

            totalPrice += moviePricePredictedReturn + finePrice + moviePriceLateReturn * multiplyBy;
        }
        labelValueValorTotal.setText(
                new DecimalFormat("#0.00").format(
                        BigDecimal.valueOf(totalPrice)
                                .divide(new BigDecimal(100))
                )
        );

        return "";
    }

