    public AlterarCategoriaDePreco() {
        comboBoxCategoriaDePrecoDataList = new Vector();
        comboBoxCategoriaDePreco = new AutoComboBox(comboBoxCategoriaDePrecoDataList);
        comboBoxAlterarFilmesDataList = new Vector();
        comboBoxAlterarFilmes = new AutoComboBox(comboBoxAlterarFilmesDataList);

        initComponents();

        listPrecosModel = new DefaultListModel();
        listPrecos.setModel(listPrecosModel);

        pricesToRemoveFromDB = new ArrayList<PriceTable>();

        try {
            conn = DriverManager.getConnection("jdbc:sqlite:local/designedDb.db");
            stmt = conn.createStatement();
            stmt.setQueryTimeout(30);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        //populate comboBoxCategoriaDePreco and comboBoxAlterarFilmes
        comboBoxCategoriaDePrecoDataList.add(new PriceCategoryTable()); //insert the first empty string
        comboBoxAlterarFilmesDataList.add(new PriceCategoryTable()); //insert the first empty string
        try {
            prepStmt = conn.prepareStatement("SELECT *"
                    + " FROM TB_PRICE_CATEGORY"
                    + " ORDER BY PRI_CAT_DESCRIPTION");
            rs = prepStmt.executeQuery();

            while (rs.next()) {
                PriceCategoryTable currentPriceCategory = new PriceCategoryTable(
                        rs.getInt("PRI_CAT_ID"),
                        rs.getString("PRI_CAT_DESCRIPTION"),
                        rs.getString("PRI_CAT_NOTES"),
                        rs.getInt("PRI_CAT_AMOUNT_OF_MOVIES_USING"),
                        rs.getInt("PRI_CAT_ACTIVE"),
                        rs.getLong("PRI_CAT_FINE_PRICE"),
                        rs.getInt("PRI_CAT_MULTIPLY_BY")
                );
                comboBoxCategoriaDePrecoDataList.add(currentPriceCategory);
                comboBoxAlterarFilmesDataList.add(currentPriceCategory);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        comboBoxCategoriaDePreco.setDataList(comboBoxCategoriaDePrecoDataList);
        comboBoxAlterarFilmes.setDataList(comboBoxAlterarFilmesDataList);
    }

