    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        comboBoxCategoriaDePrecoActionPerformed(evt); //to make sure it's selected.

        PriceCategoryTable selectedPriceCategory = (PriceCategoryTable) comboBoxCategoriaDePreco.getSelectedItem();

        if (selectedPriceCategory.isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Favor selecionar uma categoria de preço para \"Buscar\".",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else {
            //load all the data of the selectedPriceCategory
            labelValueQuantidadeDeFilmesUsandoEstaCategoria.setText(
                    selectedPriceCategory.getAmountOfMoviesUsing()
                    + " filme(s)"
            );
            textFieldNovaDescricao.setText(selectedPriceCategory.getDescription());
            checkBoxCategoriaDePrecoAtiva.setSelected(selectedPriceCategory.getActive() == 1);
            textAreaNovasAnotacoes.setText(selectedPriceCategory.getNotes());
            formattedTextFieldFinePrice.setText(
                    new DecimalFormat("#0.00").format(
                            BigDecimal.valueOf(selectedPriceCategory.getFinePrice())
                                    .divide(new BigDecimal(100))
                    )
            );
            labelValuePrecosDosDiasMultiplicadosPor.setText(
                    Integer.toString(selectedPriceCategory.getMultiplyBy())
            );

            listPrecosModel.clear();
            try {
                prepStmt = conn.prepareStatement("SELECT *"
                        + " FROM TB_PRICE"
                        + " WHERE PRI_TB_PRICE_CATEGORY_ID = ?" //1
                        + " ORDER BY PRI_AMOUNT_OF_DAYS");
                prepStmt.setInt(1, selectedPriceCategory.getId());
                rs = prepStmt.executeQuery();

                while (rs.next()) {
                    listPrecosModel.addElement(new PriceTable(
                            rs.getInt("PRI_ID"),
                            rs.getInt("PRI_AMOUNT_OF_DAYS"),
                            rs.getLong("PRI_PRICE"),
                            rs.getInt("PRI_TB_PRICE_CATEGORY_ID"),
                            selectedPriceCategory.getMultiplyBy()
                    ));
                }
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_buttonBuscarActionPerformed

