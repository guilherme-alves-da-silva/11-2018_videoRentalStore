    private void buttonFinalizarAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarAlteracaoActionPerformed
        PriceCategoryTable selectedPriceCategory = (PriceCategoryTable) comboBoxCategoriaDePreco.getSelectedItem();
        PriceCategoryTable selectedCategoryToChangeMoviesTo = (PriceCategoryTable) comboBoxAlterarFilmes.getSelectedItem();

        //trimming a mandatory field, meaning a field that has an "*" preceding the label.
        textFieldNovaDescricao.setText(textFieldNovaDescricao.getText().trim());

        if (formattedTextFieldFinePrice.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Finalizar Alteração\", favor inserir o valor da multa.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (selectedPriceCategory.isEmptyString()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Antes de clicar em \"Finalizar Alteração\", favor selecionar uma categoria de preço, e clicar em \"Buscar\".",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (textFieldNovaDescricao.getText().isEmpty()
                || listPrecos.getModel().getSize() == 0) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nOs campos * são obrigatórios!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (!textFieldNovaDescricao.getText().equals(selectedPriceCategory.getDescription())
                && comboBoxCategoriaDePrecoContainsDescription(textFieldNovaDescricao.getText())) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nJá existe uma categoria de preço chamada \"" + textFieldNovaDescricao.getText() + "\" !",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (new BigDecimal(formattedTextFieldFinePrice.getText())
                .compareTo(BigDecimal.ZERO) < 0) {

            JOptionPane.showMessageDialog(
                    null,
                    "ERRO!\nNão é possível inserir um valor negativo!",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (new BigDecimal(formattedTextFieldFinePrice.getText())
                .multiply(new BigDecimal(100))
                .compareTo(new BigDecimal(Long.MAX_VALUE)) >= 0) {

            JOptionPane.showMessageDialog(
                    null,
                    "Favor inserir um valor abaixo de \"R$ " + new BigDecimal(Long.MAX_VALUE).divide(new BigDecimal(100)) + "\"",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );

        } else if (JOptionPane.showOptionDialog(
                null,
                "Tem certeza que deseja finalizar?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new String[]{"Sim", "Não"},
                "Sim") == JOptionPane.YES_OPTION) {

            try {
                prepStmt = conn.prepareStatement("UPDATE TB_PRICE_CATEGORY SET "
                        + "PRI_CAT_DESCRIPTION = ?," //1
                        + "PRI_CAT_NOTES = ?," //2
                        + "PRI_CAT_AMOUNT_OF_MOVIES_USING = ?," //3
                        + "PRI_CAT_ACTIVE = ?," //4
                        + "PRI_CAT_FINE_PRICE = ?," //5
                        + "PRI_CAT_MULTIPLY_BY = ? " //6
                        + "WHERE PRI_CAT_ID = ?"); //7
                prepStmt.setString(1, textFieldNovaDescricao.getText());
                prepStmt.setString(2, textAreaNovasAnotacoes.getText());

                /*
                 * the PRI_CAT_AMOUNT_OF_MOVIES_USING is already changed here even though the rest
                 * of the changes, if a new priceCategory was selected, are done a few lines down.
                 * the change is done here already because if i do the change later the DB will need
                 * to find PRI_CAT_ID again just to change the PRI_CAT_AMOUNT_OF_MOVIES_USING
                 * attribute.
                 */
                prepStmt.setInt(3,
                        !selectedCategoryToChangeMoviesTo.isEmptyString()
                        && selectedCategoryToChangeMoviesTo.getId() != selectedPriceCategory.getId()
                        ? 0
                        : selectedPriceCategory.getAmountOfMoviesUsing()
                );

                prepStmt.setInt(4, checkBoxCategoriaDePrecoAtiva.isSelected() ? 1 : 0);
                prepStmt.setLong(5,
                        new BigDecimal(formattedTextFieldFinePrice.getText())
                                .multiply(new BigDecimal(100))
                                .longValue()
                );
                prepStmt.setInt(6, Integer.parseInt(labelValuePrecosDosDiasMultiplicadosPor.getText()));
                prepStmt.setInt(7, selectedPriceCategory.getId());
                prepStmt.executeUpdate();

                //insert each new price for the price category
                for (int i = 0; i < listPrecosModel.getSize(); i++) {
                    PriceTable currentPrice = (PriceTable) listPrecosModel.getElementAt(i);

                    if (currentPrice.isToInsert()) {
                        prepStmt = conn.prepareStatement("INSERT INTO TB_PRICE("
                                + "PRI_AMOUNT_OF_DAYS," //1
                                + "PRI_PRICE," //2
                                + "PRI_TB_PRICE_CATEGORY_ID" //3
                                + ") VALUES(?,?,?)");
                        prepStmt.setInt(1, currentPrice.getAmountOfDays());
                        prepStmt.setLong(2, currentPrice.getPrice());
                        prepStmt.setInt(3, selectedPriceCategory.getId());
                        prepStmt.executeUpdate();
                    }
                }

                //deleting each price that the user removed for the price category
                for (int i = 0; i < pricesToRemoveFromDB.size(); i++) {
                    prepStmt = conn.prepareStatement("DELETE"
                            + " FROM TB_PRICE"
                            + " WHERE PRI_ID = ?"); //1
                    prepStmt.setInt(1, pricesToRemoveFromDB.get(i).getId());
                    prepStmt.executeUpdate();
                }

                //if some category is selected in the comboBoxAlterarFilmes
                if (!selectedCategoryToChangeMoviesTo.isEmptyString()) {
                    if (selectedCategoryToChangeMoviesTo.getId() == selectedPriceCategory.getId()) {

                        JOptionPane.showMessageDialog(null, "A categoria de preço selecionada para ser a nova categoria nos filmes, é a mesma"
                                + "\ncategoria de preço sendo alterada, portanto não vai ser mudada.");

                    } else {
                        /*
                         * change all the movies that are using the price category selected in the
                         * comboBoxCategoriaDePreco, to the price category selected in the
                         * comboBoxAlterarFilmes.
                         */
                        prepStmt = conn.prepareStatement("UPDATE TB_MOVIE SET"
                                + " MOV_TB_PRICE_CATEGORY_ID = ?" //1
                                + " WHERE MOV_TB_PRICE_CATEGORY_ID = ?"); //2
                        prepStmt.setInt(1, selectedCategoryToChangeMoviesTo.getId());
                        prepStmt.setInt(2, selectedPriceCategory.getId());
                        prepStmt.executeUpdate();

                        /*
                         * update the PRI_CAT_AMOUNT_OF_MOVIES_USING attribute of the price
                         * category selected in the comboBoxAlterarFilmes.
                         */
                        prepStmt = conn.prepareStatement("UPDATE TB_PRICE_CATEGORY SET"
                                + " PRI_CAT_AMOUNT_OF_MOVIES_USING = ?" //1
                                + " WHERE PRI_CAT_ID = ?"); //2
                        prepStmt.setInt(1,
                                selectedCategoryToChangeMoviesTo.getAmountOfMoviesUsing()
                                + selectedPriceCategory.getAmountOfMoviesUsing()
                        );
                        prepStmt.setInt(2, selectedCategoryToChangeMoviesTo.getId());
                        prepStmt.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Alteração dos filmes bem sucedida.");
                    }
                }

                JOptionPane.showMessageDialog(null, "Alteração da Categoria de Preço bem sucedida.");
                new GerenciadorDeEmprestimosDeFilmes().setVisible(true);
                super.dispose();

            } catch (SQLException e) {
                System.out.println(e.getMessage());
            } finally {
                try {
                    if (conn != null) {
                        conn.close();
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_buttonFinalizarAlteracaoActionPerformed

